-- 科目期初余额表：用于资产负债表"年初余额"列
-- 每个用户每个科目可有一条期初余额记录，方向与科目性质一致（借/贷）
CREATE TABLE IF NOT EXISTS account_opening_balances (
  id           BIGSERIAL PRIMARY KEY,
  user_id      UUID NOT NULL REFERENCES users(id),
  account_id   VARCHAR(16) NOT NULL REFERENCES accounts(id),
  year         INT NOT NULL,
  debit_balance  NUMERIC(12,2) NOT NULL DEFAULT 0,
  credit_balance NUMERIC(12,2) NOT NULL DEFAULT 0,
  UNIQUE (user_id, account_id, year)
);

CREATE INDEX idx_opening_bal_user ON account_opening_balances(user_id, year);
